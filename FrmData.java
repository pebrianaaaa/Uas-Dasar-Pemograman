    private void CariData(){
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM barminum WHERE " 
                    + cmbCari.getSelectedItem().toString() + 
                    " LIKE '%" + txtCari.getText() + "%'");
            
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No.");
            model.addColumn("IDPESANAN");
            model.addColumn("Pesanan");
            model.addColumn("Total");
            model.addColumn("Atas Nama");
            
            int no = 1;
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while (rs.next()) {
                Object[] data = {
                    no ++,
                   rs.getString("IDPESANAN"),
                    rs.getString("Pesanan"),
                    rs.getString("Total"),
                    rs.getString("Atas Nama")
                };
                model.addRow(data);
                tblData.setModel(model);
            }
            
        } catch (Exception e) {
        }
    }
    
    
