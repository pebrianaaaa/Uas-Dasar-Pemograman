    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            st = cn.createStatement();
            if (txtID.getText().equals("") || 
                    txtPesanan.getText().equals("") ||
                    txtTotal
                    .getText().equals("") || txtAtm.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data tidak bolehh kosong", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
                
            }
            
            if (btnSimpan.getText() == "Simpan") {
                String cek = "SELECT * FROM barminum WHERE IDPESANAN = '" + txtID.getText() + "'";
                rs =  st.executeQuery(cek);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "IDPESANAN ini telah dipakai");
                    
                }else{
                    String sql  = "INSERT INTO barminum VALUES ('" + txtID.getText() + 
                            "','" + txtPesanan.getText() +
                            "','" + txtTotal.getText() +
                            "','" + txtAtm.getText() +"')";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                    Bersih();
                    TampilData();
                }
                
                    
            }else{
                //aksi ubah data
                String update = "UPDATE barminum SET Pesanan = '" + txtPesanan.getText() +
                        "', Total = '" + txtTotal.getText() + 
                        "', Atas nama =  '" + txtAtm.getText() +
                        "' WHERE IDPesanan = '" + txtID.getText() + "'";
                st.executeUpdate(update);
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                Bersih();
                TampilData();
                
            }
                    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

